db.getCollection('metertenants').aggregate([

    {
        $lookup: {
            from: "users",
            let: {
                "user": "$createdBy"
            },
            pipeline: [{
                $match: {
                    $expr: {
                        $and: [{
                            $eq: ["$_id", "$$user"]
                        }, ]
                    }
                }
            }, {
                $project: {
                    username: 1
                }
            }],
            as: "userDetails"
        }
    },
    {
        $unwind: {
            path: "$userDetails",
            preserveNullAndEmptyArrays: true
        }
    },


    {
        $lookup: {
            from: "meters",
            let: {
                "meter": "$meterSerialNumberID"
            },
            pipeline: [{
                $match: {
                    $expr: {
                        $and: [{
                            $eq: ["$_id", "$$meter"]
                        }, ]
                    }
                }
            }],
            as: "meterdet"
        }
    },
    {
        $unwind: {
            path: "$meterdet",
            preserveNullAndEmptyArrays: true
        }
    },
    {
        $group: {
            _id: "$tenantID",
            data: {
                $push: "$$ROOT"
            }
        }
    },
    {
        $lookup: {
            from: "tenants",
            let: {
                "tenant": "$_id"
            },
            pipeline: [{
                $match: {
                    $expr: {
                        $and: [{
                            $eq: ["$_id", "$$tenant"]
                        }, ]
                    }
                }
            }],
            as: "tenentdet"
        }
    },
    {
        $unwind: {
            path: "$tenentdet",
            preserveNullAndEmptyArrays: true
        }
    },


])

